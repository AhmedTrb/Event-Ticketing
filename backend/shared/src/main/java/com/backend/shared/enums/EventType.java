package com.backend.shared.enums;

/**
 * Represents the type/category of an event.
 * Used to differentiate between different event categories in the ticketing
 * system.
 */
public enum EventType {
    /**
     * Movie screening event
     */
    MOVIE,

    /**
     * Concert or live music performance
     */
    CONCERT,

    /**
     * Sports event (football, basketball, etc.)
     */
    SPORTS_EVENT
}
